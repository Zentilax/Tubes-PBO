    private void SaveBookingButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveBookingButtonActionPerformed
        // TODO add your handling code here:
        Object[] custinfo = mmcontroller.getUserInfo();
        int custid = (int)custinfo[0];
        int selectedRowIndex = TrainListTable.getSelectedRow();
        if (selectedRowIndex != -1){
            int trainID = (int)TrainListTable.getValueAt(selectedRowIndex,0);
            selectedRowIndex = WagonListTable.getSelectedRow();
            if (selectedRowIndex != -1){
                int wagonID = (int)WagonListTable.getValueAt(selectedRowIndex,0);
                int remseat = (int)WagonListTable.getValueAt(selectedRowIndex, 5);
                int seats = (int)SeatSpinner.getValue();
                if (seats <= remseat){
                    if (mmcontroller.bookTrain(custid, trainID, wagonID, seats)){
                        JOptionPane.showMessageDialog(this, "Booking Successful");             
                        this.dispose();
                        jDialog1.dispose();
                        mmcontroller.SendNotificationSMS();
                        mmcontroller.SendConfirmationEmail();
                        // Open the main menu
                        UserMainMenu mainMenu = new UserMainMenu();
                        mainMenu.setVisible(true);
                    }else{
                        JOptionPane.showMessageDialog(this, "Insertion Unsuccessful");
                    }
                }else{
                    JOptionPane.showMessageDialog(this, "Not Enough Capacity");
                }
            }
        }
        
        
        
        //mmcontroller.bookTrain(custID, trainID, ABORT, WIDTH, WIDTH)
    }//GEN-LAST:event_SaveBookingButtonActionPerformed

