    private void TrainListTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TrainListTableMouseClicked
        // TODO add your handling code here:
        int selectedRowIndex = TrainListTable.getSelectedRow();
        if (selectedRowIndex != -1){
            int TrainID = (int)TrainListTable.getValueAt(selectedRowIndex,0);
            WagonListTable.setModel(mmcontroller.getWagonsForTrain(TrainID));
            Object[] jadwal = mmcontroller.getScedulefromTrainID(TrainID);
            LabelHari.setText((String)jadwal[0]);
            LabelJam.setText((String)jadwal[1]);
        }
        
    }//GEN-LAST:event_TrainListTableMouseClicked

